package io.mixeway.pojo;


public class InfraVulnListDto {

	String infraVuln;
	Long occurance;
	String description;
	String severity;
	public String getInfraVuln() {
		return infraVuln;
	}
	public void setInfraVuln(String infraVuln) {
		this.infraVuln = infraVuln;
	}
	public Long getOccurance() {
		return occurance;
	}
	public void setOccurance(Long occurance) {
		this.occurance = occurance;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	
	
		
}
